#include "libft.h"

t_bool	ft_nfree(void ***p, t_len n)
{
	if (!*p)
		return (0);
	while (n--)
	{
		if ((*p)[n])
			free((*p)[n]);
	}
	free(*p);
	*p = 0;
	return (0);
}
