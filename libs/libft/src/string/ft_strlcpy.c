#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	t_len			s_len;
	unsigned int	i;

	if (!size || fts_strlen(&s_len, src))
		return (0);
	i = 0;
	while (i < size - 1 && src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (s_len);
}
